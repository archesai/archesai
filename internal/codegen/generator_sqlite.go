package codegen

import (
	"bytes"
	"fmt"
	"path/filepath"
	"strings"

	"github.com/archesai/archesai/internal/parsers"
)

// SQLiteGenerator generates SQLite repository implementations.
type SQLiteGenerator struct{}

// Name returns the generator name.
func (g *SQLiteGenerator) Name() string { return "sqlite" }

// Priority returns the generator priority.
func (g *SQLiteGenerator) Priority() int { return PriorityNormal }

// Generate creates SQLite repository code for each entity schema.
func (g *SQLiteGenerator) Generate(ctx *GeneratorContext) error {
	// Generate db.gen.go
	dbContent := `// Code generated by archesai. DO NOT EDIT.

package repositories

import (
	"database/sql"
)

func New(db *sql.DB) *Queries {
	return &Queries{db: db}
}

type Queries struct {
	db *sql.DB
}
`
	dbPath := filepath.Join("infrastructure", "sqlite", "repositories", "db.gen.go")
	if err := ctx.Storage.WriteFile(dbPath, []byte(dbContent), 0644); err != nil {
		return fmt.Errorf("failed to write SQLite db file: %w", err)
	}

	internalContext := ctx.InternalContext()
	for _, schema := range ctx.SpecDef.Schemas {
		if schema.XCodegenSchemaType != parsers.XCodegenSchemaTypeEntity {
			continue
		}

		if err := generateSQLQueriesForSchema(ctx, schema, "sqlite"); err != nil {
			return fmt.Errorf("failed to generate SQLite queries for %s: %w", schema.Name, err)
		}

		modelImportPath, repositoryInterface := getRepositoryImportPaths(
			ctx.ProjectName,
			internalContext,
			schema,
		)
		data := &RepositoriesTemplateData{
			Entity:              schema,
			ProjectName:         ctx.ProjectName,
			ModelImportPath:     modelImportPath,
			RepositoryInterface: repositoryInterface,
		}

		var buf bytes.Buffer
		if err := ctx.Renderer.Render(&buf, "sqlite.go.tmpl", data); err != nil {
			return fmt.Errorf("failed to render SQLite repository for %s: %w", schema.Name, err)
		}

		outputPath := filepath.Join(
			"infrastructure",
			"sqlite",
			"repositories",
			strings.ToLower(schema.Name)+"_repository.gen.go",
		)
		if err := ctx.Storage.WriteFile(outputPath, buf.Bytes(), 0644); err != nil {
			return fmt.Errorf("failed to write SQLite repository for %s: %w", schema.Name, err)
		}
	}
	return nil
}
